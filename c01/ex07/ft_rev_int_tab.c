void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	spare_ch;

	i = 0;
	while (i < size / 2)
	{
		spare_ch = *(tab + i);
		*(tab + i) = *(tab + (size -1 - i));
		*(tab + (size -1 - i)) = spare_ch;
		i++;
	}
}
